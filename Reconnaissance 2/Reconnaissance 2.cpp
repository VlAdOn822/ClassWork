#include <iostream>
using namespace std;

int main()
{
	int n;
	cin >> n;
	int* arr = new int[n];
	int answ[2];
	int minDiff = INT32_MAX;

	for (int i = 0; i < n; i++)
	{
		cin >> arr[i];
	}

	for (int i = 0; i < n - 1; i++)
	{
		for (int j = i + 1; j < n; j++)
		{
			if (abs(arr[i] - arr[j]) < minDiff)
			{
				answ[0] = i + 1;
				answ[1] = j + 1;
				minDiff = abs(arr[i] - arr[j]);
			}
		}
	}
	cout << answ[0] << " " << answ[1];
}

